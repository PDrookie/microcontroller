    ADDWF INDF0, w
    MOVWF INDF1
    SUBFWB INDF0, 0
    MOVWF PREINC1
    INCF INDF1, w
    INCF POSTINC1
    ADDWF PREINC0, w
    MOVWF INDF1
    SUBFWB PREINC0, 0
    MOVWF PREINC1
    INCF INDF1, w
    INCF POSTINC1
    ADDWF PREINC0, w
    MOVWF INDF1
    SUBFWB PREINC0, 0
    MOVWF PREINC1
    INCF INDF1, w
    INCF POSTINC1
    ADDWF PREINC0, w
