List p=18f4520
    #include<p18f4520.inc>
        CONFIG OSC = INTIO67
        CONFIG WDT = OFF
        org 0x000

setup:
    MOVLW 0x07
    MOVWF 0x00A
    LFSR 0, 0x000
    LFSR 1, 0x018
    LFSR 2, 0x001
    MOVLW 0x0A
    MOVWF INDF0 ; FSR0 = 0x000
    MOVWF POSTINC2 ; FSR2 = 0x002
    MOVLW 0x03
    MOVWF POSTDEC1 ; FSR1 = 0x017
    MOVWF INDF1 ; FSR1 = 0x017

loop:
    ADDWF PREINC0, 1 ; FSR0 = 0x001
    MOVFF POSTDEC0, POSTINC2 ; FSR0 = 0x000 FSR1 = 0x003
    SUBWF POSTINC0, w ; FSR0 = 0x001
    MOVWF POSTDEC1 ; FSR1 = 0x016
    MOVWF INDF1 ; FSR1 = 0x017
    DECF 0x00A
    TSTFSZ 0x00A, 0
	GOTO loop

last:
    ADDWF PREINC0, 1
    MOVFF POSTDEC0, POSTINC2 
    SUBWF POSTINC0, w 
    MOVWF POSTDEC1 
    CLRF 0x009
end